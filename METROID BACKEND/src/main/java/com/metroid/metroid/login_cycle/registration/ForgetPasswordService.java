    @Transactional
    public Map<String, String> forgetPassword(String email) {
        HashMap<String, String> map = new HashMap<>();

        Boolean isValidEmail =
                emailValidator.test(email);

        if (!isValidEmail) {
            map.put("message", "email not valid");
            return map;

        }
        var appUser = appUserRepository.findByEmail(email);
        if (appUser.isEmpty()) {
            map.put("message", "there are no user with this email");
        } else {

            appUserRepository.changePassword(encoder.encode("12345"), email);
            emailSender.send(
                    email,
                    buildEmail(appUser.get().getFirstName()));
            map.put("message", "success");
        }
        return map;


    }

